def on_forever():
    if controller.left.is_pressed():
        mySprite.set_velocity(-10, 0)
    elif controller.right.is_pressed():
        mySprite.set_velocity(10, 0)
    elif controller.up.is_pressed():
        mySprite.set_velocity(0, -10)
    elif controller.down.is_pressed():
        mySprite.set_velocity(0, 10)
    else:
        mySprite.set_velocity(0, 0)
forever(on_forever)
